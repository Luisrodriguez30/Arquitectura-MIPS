# Tabla de Instrucciones de Prueba - DPTR Tipo R
# Registros $0-$5 inicializados con valores 10-15
# $0=10, $1=11, $2=12, $3=13, $4=14, $5=15

# 1. add $8,$0,$1   -> R8 = 10+11 = 21
add $8,$0,$1

# 2. add $9,$2,$3   -> R9 = 12+13 = 25
add $9,$2,$3

# 3. sub $10,$3,$1  -> R10 = 13-11 = 2
sub $10,$3,$1

# 4. sub $11,$4,$2  -> R11 = 14-12 = 2
sub $11,$4,$2

# 5. and $12,$0,$1  -> R12 = 10 AND 11 = 10
and $12,$0,$1

# 6. and $13,$2,$3  -> R13 = 12 AND 13 = 12
and $13,$2,$3

# 7. or $14,$0,$1   -> R14 = 10 OR 11 = 11
or $14,$0,$1

# 8. or $15,$2,$3   -> R15 = 12 OR 13 = 15
or $15,$2,$3

# 9. slt $16,$0,$1  -> R16 = (10 < 11) = 1
slt $16,$0,$1

# 10. slt $17,$1,$0 -> R17 = (11 < 10) = 0
slt $17,$1,$0
